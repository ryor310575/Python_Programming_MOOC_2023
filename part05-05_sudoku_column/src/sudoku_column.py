def column_correct(sudoku: list, column_no: int)->bool:
    col_corr=True
    col_list=[]
    for row in sudoku:
        col_list.append(row[column_no])
    for sk_number in range(1,10):
        if col_list.count(sk_number)>1:
            col_corr = False
    return col_corr
